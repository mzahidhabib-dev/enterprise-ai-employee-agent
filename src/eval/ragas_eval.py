# src/eval/ragas_eval.py
"""
RAGAS evaluation script for the AI Employee Agent.

Runs the golden dataset through the agent graph and compares the actual
outputs against the expected ground truths using the RAGAS framework
(Faithfulness, Answer Relevancy, Context Recall).
"""

import uuid
from typing import List
from pydantic import BaseModel

from datasets import Dataset
from ragas import evaluate
from ragas.metrics import faithfulness, answer_relevancy, context_recall

from src.agent.graph import agent_graph
from src.agent.llm import llm
from src.db.database import SessionLocal
from src.db.models import EvalResult as EvalResultModel
from src.eval.report_generator import generate_html_report


class EvalResult(BaseModel):
    """Pydantic model representing the evaluation return payload."""
    accuracy_pct: float
    faithfulness_score: float
    relevancy_score: float
    recall_score: float
    failed_cases: List[str]


def run_evaluation(test_cases: list[dict]) -> EvalResult:
    """
    Executes the golden dataset through the LangGraph and evaluates 
    the quality of the responses using RAGAS.
    """
    questions = []
    answers = []
    contexts = []
    ground_truths = []
    
    failed_cases = []
    correct_classifications = 0
    total_cases = len(test_cases)
    
    print(f"Starting evaluation of {total_cases} test cases...")

    # 1. Run the agent graph for each test case
    for case in test_cases:
        case_id = case.get("test_id", "unknown")
        email_data = case.get("email", {})
        expected_action = case.get("expected_output", {}).get("action")
        
        # Unique thread ID to keep LangGraph checkpointer memory clean
        config = {"configurable": {"thread_id": f"eval_{uuid.uuid4()}"}}
        
        # Inject the single email into state. The fetch node will route appropriately.
        state = {"emails": [email_data], "processed_count": 0, "errors": []}
        
        try:
            result = agent_graph.invoke(state, config=config)
        except Exception as e:
            print(f"Error invoking graph for {case_id}: {e}")
            failed_cases.append(case_id)
            continue
            
        # Extract classification accuracy
        classification = result.get("classification")
        actual_action = classification.action.value if classification else None
        
        if actual_action == expected_action:
            correct_classifications += 1
        else:
            failed_cases.append(case_id)
            
        # 2. Extract texts for RAGAS
        question = email_data.get("body", "")
        
        summary = classification.summary if classification else "No summary"
        draft = result.get("draft_reply", "")
        # The 'answer' is the full output generated by the agent
        answer = f"Summary: {summary}\nDraft: {draft}" if draft else f"Summary: {summary}"
        
        # The 'context' is the data provided to the agent
        context_str = f"Subject: {email_data.get('subject', '')}\nFrom: {email_data.get('from', '')}"
        
        questions.append(question)
        answers.append(answer)
        contexts.append([context_str]) # RAGAS expects context as a list of strings
        ground_truths.append(expected_action)

    # Calculate baseline classification accuracy
    accuracy_pct = (correct_classifications / total_cases) * 100.0 if total_cases > 0 else 0.0

    # 3. Build RAGAS HuggingFace Dataset
    # ragas expects ground_truths to be a list of lists of strings
    data_dict = {
        "question": questions,
        "answer": answers,
        "contexts": contexts,
        "ground_truths": [[gt] for gt in ground_truths]
    }
    
    dataset = Dataset.from_dict(data_dict)
    
    # 4. Run RAGAS evaluation using our Gemini LLM as the judge
    print("Evaluating RAGAS metrics...")
    try:
        ragas_result = evaluate(
            dataset=dataset,
            metrics=[faithfulness, answer_relevancy, context_recall],
            llm=llm
        )
        f_score = ragas_result.get('faithfulness', 0.0)
        r_score = ragas_result.get('answer_relevancy', 0.0)
        c_score = ragas_result.get('context_recall', 0.0)
    except Exception as e:
        print(f"RAGAS evaluation failed (perhaps LLM rate limit): {e}")
        f_score = r_score = c_score = 0.0
    
    final_result = EvalResult(
        accuracy_pct=accuracy_pct,
        faithfulness_score=f_score,
        relevancy_score=r_score,
        recall_score=c_score,
        failed_cases=failed_cases
    )
    
    # 5. Save results to PostgreSQL EvalResult table
    try:
        with SessionLocal() as db:
            db_result = EvalResultModel(
                accuracy_pct=accuracy_pct,
                faithfulness_score=f_score,
                relevancy_score=r_score,
                recall_score=c_score,
                failed_cases=failed_cases
            )
            db.add(db_result)
            db.commit()
            print("Evaluation results saved to database.")
    except Exception as e:
        print(f"Error saving EvalResult to database: {e}")
        
    # 6. Generate Client HTML Report
    try:
        report_path = generate_html_report(final_result)
        print(f"Client HTML report generated at: {report_path}")
    except Exception as e:
        print(f"Error generating HTML report: {e}")
        
    return final_result

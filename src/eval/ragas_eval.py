class EvalResult(BaseModel):
    """Pydantic model representing the evaluation return payload."""
    accuracy_pct: float
    faithfulness_score: float
    relevancy_score: float
    recall_score: float
    failed_cases: List[str]


def run_evaluation(test_cases: list[dict]) -> EvalResult:
    """
    Executes the golden dataset through the LangGraph and evaluates 
    the quality of the responses using RAGAS.
    """
    questions = []
    answers = []
    contexts = []
    ground_truths = []
    
    failed_cases = []
    correct_classifications = 0
    total_cases = len(test_cases)
    
    print(f"Starting evaluation of {total_cases} test cases...", flush=True)

    # 1. Run the agent graph for each test case
    for case in test_cases:
        case_id = case.get("test_id", "unknown")
        email_data = case.get("email", {})
        expected_action = case.get("expected_output", {}).get("action")
        
        # Unique thread ID to keep LangGraph checkpointer memory clean
        config = {"configurable": {"thread_id": f"eval_{uuid.uuid4()}"}}
        
        # Inject the single email into state. The fetch node will route appropriately.
        state = {"emails": [email_data], "processed_count": 0, "errors": []}
        
        try:
            result = agent_graph.invoke(state, config=config)
        except Exception as e:
            print(f"Error invoking graph for {case_id}: {e}", flush=True)
            failed_cases.append(case_id)
            continue
            
        # Extract classification accuracy
        classification = result.get("classification")
        actual_action = classification.action.value if classification and hasattr(classification, 'action') else None
        
        if actual_action == expected_action:
            correct_classifications += 1
        else:
            print(f"\n[EVAL FAILURE] Case {case_id} Failed!", flush=True)
            print(f"Email: {email_data.get('body')}", flush=True)
            print(f"Expected: {expected_action}", flush=True)
            print(f"Actual Prediction: {actual_action}", flush=True)
            failed_cases.append(case_id)
            
        # 2. Extract texts for RAGAS
        question = email_data.get("body", "")
        
        summary = getattr(classification, 'summary', "No summary") if classification else "No summary"
        draft = result.get("draft_reply", "")
        # The 'answer' is the full output generated by the agent
        answer = f"Summary: {summary}\nDraft: {draft}" if draft else f"Summary: {summary}"
        
        # The 'context' is the data provided to the agent
        context_str = f"Subject: {email_data.get('subject', '')}\nFrom: {email_data.get('from', '')}"
        
        questions.append(question)
        answers.append(answer)
        contexts.append([context_str]) # RAGAS expects context as a list of strings
        ground_truths.append(expected_action)

    # Calculate baseline classification accuracy
    accuracy_pct = (correct_classifications / total_cases) * 100.0 if total_cases > 0 else 0.0

    # 3. Custom Evaluation Loop (Replacing RAGAS)
    print("Evaluating custom LLM metrics...", flush=True)
    f_scores = []
    c_scores = []
    
    import time
    for q, a, c, g in zip(questions, answers, contexts, ground_truths):
        # Faithfulness: Is the answer derived ONLY from the context?
        f_prompt = f"Given the context:\n{c[0]}\n\nAnd the answer:\n{a}\n\nIs the answer completely supported by the context? Reply with strictly 1.0 for Yes, 0.0 for No."
        
        # Context Recall: Does the answer address the question based on ground truth?
        c_prompt = f"Given the question:\n{q}\n\nAnd the expected ground truth:\n{g}\n\nAnd the actual answer:\n{a}\n\nDoes the actual answer contain the expected ground truth action? Reply with strictly 1.0 for Yes, 0.0 for No."
        
        try:
            time.sleep(10) # Rate limit protection (15 RPM)
            f_res = llm.invoke(f_prompt).content.upper()
            f_scores.append(1.0 if ("1.0" in f_res or "YES" in f_res or "1" in f_res) else 0.0)
            
            time.sleep(10) # Rate limit protection (15 RPM)
            c_res = llm.invoke(c_prompt).content.upper()
            c_scores.append(1.0 if ("1.0" in c_res or "YES" in c_res or "1" in c_res) else 0.0)
        except Exception as e:
            print(f"LLM Eval error: {e}", flush=True)
            f_scores.append(0.0)
            c_scores.append(0.0)
            
    f_score = sum(f_scores) / len(f_scores) if f_scores else 0.0
    c_score = sum(c_scores) / len(c_scores) if c_scores else 0.0
    r_score = 0.0
    
    final_result = EvalResult(
        accuracy_pct=accuracy_pct,
        faithfulness_score=f_score,
        relevancy_score=r_score,
        recall_score=c_score,
        failed_cases=failed_cases
    )
    
    # 5. Save results to PostgreSQL EvalResult table
    try:
        with SessionLocal() as db:
            db_result = EvalResultModel(
                accuracy_pct=accuracy_pct,
                faithfulness_score=f_score,
                relevancy_score=r_score,
                recall_score=c_score,
                failed_cases=failed_cases
            )
            db.add(db_result)
            db.commit()
            print("Evaluation results saved to database.")
    except Exception as e:
        print(f"Error saving EvalResult to database: {e}")
        
    # 6. Generate Client HTML Report
    try:
        report_path = generate_html_report(final_result)
        print(f"Client HTML report generated at: {report_path}")
    except Exception as e:
        print(f"Error generating HTML report: {e}")
        
    return final_result

def run_scheduled_evaluation():
    """Wrapper function to load the dataset and run the evaluation, used by APScheduler."""
    try:
        # Load dataset
        dataset_path = os.path.join(os.path.dirname(__file__), "golden_dataset.json")
        if not os.path.exists(dataset_path):
            print(f"Error: Golden dataset not found at {dataset_path}. Please run generate_dataset.py first.")
            return None
            
        with open(dataset_path, "r", encoding="utf-8") as f:
            test_cases = json.load(f)
            
        print(f"Loaded {len(test_cases)} test cases from golden_dataset.json.")
        
        # We only run a small sample (e.g., 5 cases) during scheduled evaluations to save costs,
        # unless configured otherwise.
        sample_size = 2
        sample_size = 2
        sample_cases = test_cases[:sample_size]
        
        return run_evaluation(sample_cases)
    except Exception as e:
        import traceback
        print(f"Error running scheduled evaluation: {e}\n{traceback.format_exc()}", flush=True)
        return None
